// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////
function loop(value, testFunction, updateFunction, body) {
  
  for (var i = value; testFunction(i); i = updateFunction(i)) {
    
    body(i);
    
  }
}


// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(array, test) {
  
  for (let i = 0; i < array.length; i++) {
    
    if (!test(array[i])) {
      
      return false;
    }
  }
  return true;
}

