// it is even and returns a boolean
// use recursive method to change all numbers that are not 0 and 1 to 0 and 1 to be tested



function isEven(number) {
  if (number === 0) {
   return true;
} else if (number === 1) {
    return false;
} else if (number < 0) {
  return isEven(-number);
} else {
  return isEven(number - 2);
}
}
  


