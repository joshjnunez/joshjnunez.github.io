// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
  
  var list = null;
  
  for(let i = array.length-1; i >= 0; i--) {
    list = {
      value: array[i],
      rest: list
    };
  }
    return list;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list) {
  var array = [];
  var x = list;
  
  while(x) {
    array.push(x.value);
    x = x.rest;
  }
  return array;

}

